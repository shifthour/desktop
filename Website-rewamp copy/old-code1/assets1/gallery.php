  document.addEventListener('DOMContentLoaded', function() {
    const filterButtons = document.querySelectorAll('.nav-link');
    const items = document.querySelectorAll('.gallery-filter .col-md-6');

    // Function to filter items
    function filterItems(filter) {
      items.forEach(item => {
        if (filter === 'all' || item.classList.contains(filter)) {
          item.style.display = 'block';  // Show item
        } else {
          item.style.display = 'none';  // Hide item
        }
      });
    }

    // Handle filter button clicks
    filterButtons.forEach(button => {
      button.addEventListener('click', () => {
        // Remove active class from all buttons
        filterButtons.forEach(btn => btn.classList.remove('active'));
        // Add active class to the clicked button
        button.classList.add('active');

        // Get the filter ID from the button and filter items
        const filter = button.id;
        filterItems(filter);
      });
    });

    // Automatically filter based on URL parameters
    function filterGalleryOnLoad() {
      const urlParams = new URLSearchParams(window.location.search);
      const filters = ['krishna', 'vashishta', 'naadam', 'vyasa', 'agastya'];
      let foundFilter = false;

      filters.forEach(filter => {
        if (urlParams.get(filter) === 'true') {
          const filterButton = document.getElementById(filter);
          if (filterButton) {
            filterButton.click();  // Simulate click for matching filter
            foundFilter = true;
          }
        }
      });

      // If no filter is found in the URL, default to 'all'
      if (!foundFilter) {
        document.getElementById('all').click();
      }
    }

    // Call the filter on page load based on URL params
    filterGalleryOnLoad();
  });

</script>


const express = require('express');
const supabase = require('../config/supabase');
const router = express.Router();

// Get all reports with filtering and pagination
router.get('/', async (req, res) => {
  try {
    const { 
      study_id, 
      report_type, 
      status, 
      generated_by,
      page = 1, 
      limit = 25,
      search 
    } = req.query;

    let query = supabase
      .from('edc_reports')
      .select(`
        *,
        edc_studies!inner(protocol_number, title),
        generated_by_user:edc_users!generated_by(first_name, last_name)
      `);

    // Apply filters
    if (study_id) query = query.eq('study_id', study_id);
    if (report_type) query = query.eq('report_type', report_type);
    if (status) query = query.eq('status', status);
    if (generated_by) query = query.eq('generated_by', generated_by);
    if (search) {
      query = query.or(`report_name.ilike.%${search}%, description.ilike.%${search}%`);
    }

    // Apply pagination
    const offset = (page - 1) * limit;
    query = query.range(offset, offset + limit - 1);

    const { data: reports, error } = await query
      .order('generated_at', { ascending: false });

    if (error) throw error;

    // Get total count for pagination
    const { count: totalCount } = await supabase
      .from('edc_reports')
      .select('*', { count: 'exact', head: true });

    res.json({
      status: 'success',
      data: reports || [],
      pagination: {
        page: parseInt(page),
        limit: parseInt(limit),
        total: totalCount,
        pages: Math.ceil(totalCount / limit)
      }
    });
  } catch (error) {
    console.error('Reports fetch error:', error);
    res.status(500).json({
      status: 'error',
      message: 'Failed to fetch reports',
      error: error.message
    });
  }
});

